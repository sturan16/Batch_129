package day13whileloopdowhileloop;

public class DoWhileLoop01 {
    public static void main(String[] args) {

        //1)While-loop
        int i=1;
                                                //While body hic calismadi.While loopda zero execution mumkundur.
        while(i<1){
            System.out.println("Sen bir while loopsun");

            i++;
        }

        //2)Do-While Loop

        int k=1;


        do{
            System.out.println("Sen bir do-while loopsun");   //Do while body calisti
            k++;                                              //Do while kullandiginizda loop body en az bir kere calisir.
        }while (k<1);                                         //Do while da "zero execution"mumkun degıldır.














    }
}
